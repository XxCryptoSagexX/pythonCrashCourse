print("------ If Statements: Simple ------")
age = 19
print('Current age: ' + str(age))
if age >= 18:
    print('\tYou are old enough to vote')
    print('\tHave you registered to vote yet?')

print("\n------ If-Else Statements ------")
age = 17
print('Current age: ' + str(age))
if age >= 18:
    print('\tYou are old enough to vote')
    print('\tHave you registered to vote yet?')
else:
    print('\tSorry, you are too young to vote.')
    print('\tPlease register to vote as soon as you turn 18!')

print("\n------ If-Elif-Else Chain ------\n")
